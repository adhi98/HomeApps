import UIKit

extension UIView {
    
    func configBorderView() {
        layer.borderColor = #colorLiteral(red: 0.8039215803, green: 0.8039215803, blue: 0.8039215803, alpha: 1)
        layer.borderWidth = 1
        layer.cornerRadius = 5
    }
}
